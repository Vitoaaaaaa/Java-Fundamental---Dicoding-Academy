package com.dicoding.javafundamental.operator;

public class OperasiAritmatika {
    public static void main(String[] args) {
        System.out.println("==Operasi Penjumlahan==");
        int hasilPenjumlahan = 5+1;
        System.out.println("Hasil Penjumlahan 5 + 1: "+hasilPenjumlahan);
        System.out.println();

        System.out.println("==Operasi Pengurangan==");
        int hasilPengurangan = 5-1;
        System.out.println("Hasil Penjumlahan 5 * 1: "+hasilPengurangan);
        System.out.println();

        System.out.println("==Operasi Perkalian==");
        int hasilPerkalian = 5*1;
        System.out.println("Hasil Perkalian 5 * 1: "+hasilPerkalian);
        System.out.println();

        System.out.println("==Operasi Pembagian==");
        int hasilPembagian = 5/1;
        System.out.println("Hasil Pembagian 5 / 1: "+hasilPembagian);
        System.out.println();

        System.out.println("==Operasi hasilSisaBagi==");
        int hasilSisaBagi = 5%2;
        int hasilSisaBagi1= 8/2;
        System.out.println("Hasil Penjumlahan 5 % 2: "+hasilSisaBagi);
        System.out.println("Hasil Penjumlahan 8 / 2: "+hasilSisaBagi1);
        System.out.println();
    }
}
